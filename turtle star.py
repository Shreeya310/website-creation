import turtle
sc=turtle.Screen()
sc.bgcolor("orange")
sc.setup(700,700)
sc.title("TURTLE library")
point=turtle.Turtle()
point.forward(200)
point.left(120)
point.forward(200)
point.left(120)
point.forward(200)
point.penup()
point.right(150)
point.forward(120)
point.pendown()
point.right(90)
point.forward(200)
point.right(120)
point.forward(200)
point.right(120)
point.forward(200)





turtle.done()
